/**
 * Calculator
 * @module calculator
 */
export {add, multiply}

/**
 * Add two numbers
 * @param {number} x - fist number
 * @param {number} y - second number
 * @return {number} - Sum of x and y
 */
const add = (x, y) => x + y;

/**
 * Multiply two numbers
 * @param {number} x - fist number
 * @param {number} y - second number
 * @return {number} - Product of x and y
 */
const multiply = (x, y) => x * y;